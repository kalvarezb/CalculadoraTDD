package com.kodea.tdd.calc;

public class Calc {

	public double add(int val1, int val2) {
		double result=val1+val2;
		return result;
	}

	public double restar(int val1, int val2) {
		double result=val1-val2;
		return result;
	}

	public double multiplicar(int val1, int val2) {
		double result=val1*val2;
		return result;
	}

	public double dividir(int val1, int val2) {
		double result=val1/val2;
		return result;
	}

}
